### 🎯 电梯演讲 (Elevator Pitch)

> "一款以${theme}为背景的${genre}游戏，玩家将在...
> 独特卖点是..."

### 🔑 核心体验

\`\`\`
玩家应该感受到:
1. 🎭 (核心情感体验 — 例: 紧张刺激)
