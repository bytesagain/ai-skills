case "$CMD" in
  concept)   cmd_concept "$@" ;;
  mechanics) cmd_mechanics "$@" ;;
  economy)   cmd_economy "$@" ;;
  level)     cmd_level "$@" ;;
  narrative) cmd_narrative "$@" ;;
  document)  cmd_document "$@" ;;
  help|--help) show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo "运行 'game.sh help' 查看帮助"
    exit 1
    ;;
esac
