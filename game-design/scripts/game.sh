cmd_mechanics() {
  local core_loop="${1:?请指定核心玩法}"
  local features="${2:?请提供特性关键词 (逗号分隔)}"

  IFS=',' read -ra feats <<< "$features"

  cat <<EOF
## ⚙️ 核心玩法设计

### 核心循环: ${core_loop}

\`\`\`
短循环 (秒级):
  输入 → 反馈 → 奖励
  │
  └─ 每次操作都有即时响应

中循环 (分钟级):
  开始 → 挑战 → 决策 → 结果
  │
  └─ 一局/一关的完整体验

长循环 (小时级):
  目标 → 积累 → 突破 → 新目标
  │
  └─ 整体成长和成就感
\`\`\`

### 🎮 机制详解

EOF

  local idx=1
  for feat in "${feats[@]}"; do
    feat=$(echo "$feat" | xargs)
    cat <<EOF
#### 机制${idx}: ${feat}

| 要素 | 设计 |
|------|------|
| 目标 | 玩家通过${feat}想要达成什么？ |
| 规则 | ${feat}的基本规则和限制 |
| 反馈 | 成功/失败的即时反馈 |
| 深度 | 简单易上手，精通需要练习 |

EOF
    idx=$((idx + 1))
  done

  cat <<'EOF'
### 📐 平衡性考量

```
难度曲线:
  难度 ↑
       │        ╱──── 挑战区 (心流)
       │      ╱
       │    ╱──── 学习区
       │  ╱
       │╱──── 新手区
       └─────────────→ 时间
```

### 🔄 系统互动

各机制之间需要产生有趣的互动:
- 机制A + 机制B = 组合效果
- 玩家可以发现非设计预期的策略
- 不同玩法风格都能找到乐趣

### ✅ 检查清单

- [ ] 30秒内能感受到乐趣
- [ ] 规则简单但策略深度高
- [ ] 有"一次更多"的上瘾循环
- [ ] 多种策略都可以获胜
- [ ] 新手和老手都有合适体验
EOF
}

cmd_economy() {
  local genre="${1:?请指定游戏类型}"
  local model="${2:?请指定商业模式 (buy-to-play|free-to-play|subscription)}"

  cat <<EOF
## 💰 游戏经济系统: ${genre}

### 商业模式: ${model}

---

### 🏦 货币体系

\`\`\`
┌──────────────────────────────────────┐
│             货币结构                  │
├──────────────────────────────────────┤
│                                      │
│  💰 软通货 (Gold)                     │
│  ├── 获取: 战斗、任务、日常            │
│  ├── 消耗: 装备、药品、升级            │
│  └── 通胀控制: 税收、沉没消耗          │
│                                      │
│  💎 硬通货 (Gems)                     │
│  ├── 获取: 付费、成就、活动            │
│  ├── 消耗: 加速、外观、特权            │
│  └── 定价: 1元=10宝石                 │
│                                      │
│  🎟️ 活动货币 (Tokens)                │
│  ├── 获取: 限时活动                   │
│  ├── 消耗: 限定奖励兑换               │
│  └── 过期: 活动结束后清零             │
│                                      │
└──────────────────────────────────────┘
\`\`\`

### ⚖️ 经济平衡

| 指标 | 目标值 | 说明 |
|------|--------|------|
| 日均产出 | 基准值 | 玩家每天能获得多少软通货 |
| 日均消耗 | ≥产出×80% | 消耗要跟上产出 |
| 付费ARPU | ¥30-50 | 付费玩家月均消费 |
| 付费率 | 3-5% | 付费玩家比例 |
| 首充价格 | ¥6 | 低门槛首充 |

EOF

  case "$model" in
    free-to-play|f2p)
      cat <<'EOF'
### 🎁 F2P 变现设计

| 付费项目 | 价格区间 | 策略 |
|---------|---------|------|
| 首充礼包 | ¥6 | 超高性价比，突破心理门槛 |
| 月卡 | ¥30 | 每日领取，培养登录习惯 |
| 通行证 | ¥68 | 赛季制，持续付费 |
| 限定外观 | ¥18-128 | 社交展示，不影响数值 |
| 抽卡/十连 | ¥60 | 保底机制，控制期望 |
| 体力/加速 | ¥1-10 | 高频小额 |

**注意**: 避免Pay-to-Win，保护免费玩家体验
EOF
      ;;
    buy-to-play|b2p)
      cat <<'EOF'
### 💳 买断制设计

| 收入来源 | 说明 |
|---------|------|
| 游戏本体 | 一次性购买 |
| DLC内容包 | 后续扩展内容 |
| 季票 | 年度内容计划 |
| 外观商店 | 纯装饰品 |
| 捆绑折扣 | 全内容合集 |

**核心**: 本体内容要完整，DLC要物超所值
EOF
      ;;
    *)
      echo "**通用建议**: 选择与游戏类型和受众匹配的变现模式"
      ;;
  esac

  cat <<'EOF'

### 🚨 经济崩溃预警

- ⚠️ 通货膨胀: 产出>消耗 → 加入消耗坑
- ⚠️ 通货紧缩: 消耗>产出 → 玩家流失
- ⚠️ 贫富差距: 老玩家碾压新玩家 → 追赶机制
- ⚠️ RMT/外挂: 破坏经济系统 → 反作弊
EOF
}

cmd_level() {
  local genre="${1:?请指定游戏类型}"
  local level_type="${2:?请指定关卡类型 (tutorial|combat|puzzle|boss|exploration)}"
  local difficulty="${3:-medium}"

  cat <<EOF
## 🗺️ 关卡设计: ${level_type}

**游戏类型**: ${genre}
**难度**: ${difficulty}

---

### 关卡结构

\`\`\`
┌─────────────────────────────────────────┐
│                关卡流程                   │
├─────────────────────────────────────────┤
│                                         │
│  入口 → 教学区 → 挑战1 → 安全区 →        │
│         挑战2 → 高潮 → 奖励 → 出口       │
│                                         │
│  节奏: 紧张 ──→ 放松 ──→ 紧张 ──→ 释放    │
│                                         │
└─────────────────────────────────────────┘
\`\`\`

### 📐 设计原则

EOF

  case "$level_type" in
    tutorial)
      cat <<'EOF'
#### 新手教程设计

1. **不要文字轰炸** — 边玩边学
2. **确保首次成功** — 降低难度让玩家获得成就感
3. **逐步引入** — 每次只教一个新机制
4. **允许失败** — 失败代价要低
5. **给予奖励** — 完成教程有丰厚奖励

```
教学节奏:
  移动 → 攻击 → 道具 → 组合技 → 特殊机制
  (每个步骤间有实践区域)
```
EOF
      ;;
    combat)
      cat <<'EOF'
#### 战斗关卡设计

1. **前戏** — 进入战斗前的紧张感营造
2. **节奏** — 战斗强度起伏波动
3. **选择** — 多种策略可以获胜
4. **奖励** — 胜利后的满足感

```
战斗节奏:
  小怪 → 精英 → 小怪 → 中BOSS → 恢复 → BOSS
  强度: ▁▃▁▅▃█
```
EOF
      ;;
    puzzle)
      cat <<'EOF'
#### 解谜关卡设计

1. **观察** — 让玩家观察到所有线索
2. **思考** — 给足思考的时间和空间
3. **尝试** — 允许试错，不要一命死
4. **顿悟** — "啊哈！"时刻是核心体验
5. **验证** — 解开后要有明确的确认

```
难度渐进:
  单一机制 → 组合机制 → 时间压力 → 多步骤 → 隐藏规则
```
EOF
      ;;
    boss)
      cat <<'EOF'
#### Boss战设计

1. **阶段化** — 分2-3个阶段，每阶段新技能
2. **可读性** — Boss的攻击要有前摇/提示
3. **惩罚窗口** — 玩家能找到输出时机
4. **学习曲线** — 死1-3次后能学会打法
5. **史诗感** — 音乐、特效、场景配合

```
Boss战结构:
  Phase 1 (100-70%): 基础招式
  Phase 2 (70-30%):  新增2-3个技能
  Phase 3 (30-0%):   狂暴/终极形态
```
EOF
      ;;
    *)
      cat <<EOF
#### ${level_type} 关卡设计

- 明确关卡目标
- 设计清晰的关键路径
- 添加可选探索区域
- 控制关卡时长
- 设置适当的检查点
EOF
      ;;
  esac

  cat <<'EOF'

### ✅ 关卡检查清单

- [ ] 目标清晰（玩家知道该做什么）
- [ ] 节奏合理（紧张和放松交替）
- [ ] 多种策略（不只一种通关方式）
- [ ] 适当难度（挑战但不至于放弃）
- [ ] 奖励感（完成后有满足感）
- [ ] 无死角（不会卡住无法前进）
EOF
}

cmd_narrative() {
  local world="${1:?请提供世界观类型}"
  local theme="${2:?请提供故事主题}"
  local structure="${3:-three-act}"

  cat <<EOF
## 📜 叙事框架设计

**世界观**: ${world}
**主题**: ${theme}
**结构**: ${structure}

---

### 🌍 世界观设定

\`\`\`
世界: ${world}
├── 物理规则 — 这个世界如何运作？
├── 社会结构 — 权力和阶层
├── 历史背景 — 过去发生了什么
├── 核心冲突 — 世界面临什么问题
└── 文化特色 — 独特的文化元素
\`\`\`

### 📖 故事结构

EOF

  case "$structure" in
    three-act|三幕式)
      cat <<EOF
#### 三幕式结构

\`\`\`
                    高潮 ★
                   / \\
      转折点1    /   \\    转折点2
         ★    /     \\    ★
        /  \\ /       \\  / \\
       /    X         \\/   \\
      /    / \\       /     \\
     /   /   \\     /       \\
    /  /     \\   /         \\
───/─/────────\\─/────────────\\─── 结局
  / /   第一幕  \\ / 第二幕       \\第三幕

第一幕 (25%): 建置
├── 日常世界
├── 冒险召唤
└── 跨越门槛

第二幕 (50%): 对抗
├── 考验与盟友
├── 最深的洞穴
└── 最大的考验

第三幕 (25%): 解决
├── 复活/转变
├── 最终决战
└── 带着宝藏回归
\`\`\`
EOF
      ;;
    多结局)
      cat <<EOF
#### 多结局分支结构

\`\`\`
                ┌── 结局A (光明)
         ┌──选择2──┤
         │       └── 结局B (灰色)
    选择1─┤
         │       ┌── 结局C (黑暗)
         └──选择3──┤
                └── 结局D (隐藏)
\`\`\`

**关键选择设计**:
- 选择没有明显的对错
- 每个选择都有代价
- 后果不是立即显现
- 多周目才能看到全部内容
EOF
      ;;
    *)
      echo "#### ${structure} 结构"
      echo ""
      echo "根据需求设计叙事结构"
      ;;
  esac

  cat <<EOF

### 🎭 主题表达: ${theme}

**核心问题**: 这个故事在问什么问题？

**主题层次**:
1. 表面: (情节层面的冲突)
2. 中层: (角色层面的成长)
3. 深层: (哲学/社会层面的思考)

### 📋 叙事设计检查

- [ ] 世界观自洽
- [ ] 主角有清晰的目标和弧光
- [ ] 主题通过故事展现而非说教
- [ ] 环境叙事（不用文字也能传达信息）
- [ ] 玩家的行动影响故事走向
EOF
}

