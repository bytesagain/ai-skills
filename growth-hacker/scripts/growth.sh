if __name__ == "__main__":
    action = sys.argv[1] if len(sys.argv) > 1 else "help"
    args = sys.argv[2:]

    if action == "aarrr":
        if len(args) < 1:
            print("❌ 用法: growth.sh aarrr <产品名>")
            sys.exit(1)
        aarrr(args[0])
    elif action == "acquisition":
        if len(args) < 2:
            print("❌ 用法: growth.sh acquisition <产品名> <预算>")
            sys.exit(1)
        acquisition(args[0], args[1])
    elif action == "retention":
        if len(args) < 1:
            print("❌ 用法: growth.sh retention <产品名>")
            sys.exit(1)
        retention(args[0])
    elif action == "viral":
        if len(args) < 1:
            print("❌ 用法: growth.sh viral <产品名>")
            sys.exit(1)
        viral(args[0])
    elif action == "help":
        show_help()
    else:
        print("❌ 未知命令: {}".format(action))
        show_help()
        sys.exit(1)
