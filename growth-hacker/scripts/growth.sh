def viral(product):
    print("""
================================================================================
              裂变增长方案 — {product}
================================================================================
日期：{date}

━━━ 🧬 裂变模型选择 ━━━

  K因子 = 每用户邀请数 × 邀请接受率
  目标：K > 1（自增长），至少 K > 0.5（有效裂变）

━━━ 🎁 方案一：邀请有礼 ━━━

  机制：老带新，双方获益

  流程：
    用户A 生成专属邀请码/链接
        ↓
    分享给好友B
        ↓
    好友B 注册并完成[关键行为]
        ↓
    A 获得 [奖励]，B 获得 [新人礼]

  奖励设计：
    - 邀请1人：[X积分/优惠券]
    - 邀请3人：[解锁高级功能]
    - 邀请10人：[月度VIP]
    - 累计邀请榜：[季度大奖]

  关键点：
    ✅ 奖励即时到账（即时满足）
    ✅ 进度可视化（邀请排行榜）
    ✅ 阶梯奖励（越邀越多）

━━━ 🛒 方案二：拼团/砍价 ━━━

  机制：社交压力+利益驱动

  玩法设计：
    - 2人团：享 8折
    - 3人团：享 6折
    - 5人团：享 3折/免费

  执行要点：
    1. 限时（24小时内成团）
    2. 实时显示拼团进度
    3. 即将成功时 Push 提醒
    4. 拼团失败自动退款

━━━ 🔓 方案三：分享解锁 ━━━

  机制：分享行为 = 使用门票

  场景：
    - 分享到朋友圈 → 解锁今日[X]次使用
    - 分享给3个好友 → 解锁[高级功能]体验
    - 每日分享 → 累计积分兑换

  注意：
    ⚠️ 不要过度（微信可能封禁诱导分享）
    ✅ 提供替代方案（付费解锁）

━━━ 🏆 方案四：UGC裂变 ━━━

  机制：让用户自发创造可传播内容

  玩法：
    1. 生成个性化海报/报告（年度总结类）
    2. 挑战赛/话题活动（带产品水印）
    3. 用户故事征集（优秀作品奖励）
    4. 模板/工具（用户创作→自然传播）

  关键：内容本身有分享价值，不靠利诱

━━━ 📱 方案五：社群裂变 ━━━

  机制：群+海报+个人号 矩阵

  流程：
    海报（朋友圈/社群）
        ↓
    扫码进群（活码工具防封）
        ↓
    群内引导：完成任务领奖励
        ↓
    用户分享海报→新用户扫码→循环

  工具清单：
    - 活码工具（群满自动换群）
    - 海报生成器（个性化参数）
    - 社群管理机器人
    - 数据追踪系统

━━━ 📊 效果评估 ━━━

  核心指标：
    - K因子（病毒系数）
    - 单个裂变用户成本
    - 裂变用户留存率（vs自然用户）
    - 裂变链路转化漏斗

  优化方向：
    - 分享率低 → 优化分享动机/文案/视觉
    - 点击率低 → 优化标题/封面/利益点
    - 转化率低 → 优化落地页/注册流程
    - 留存率低 → 检查用户质量/匹配度

================================================================================
🚀 建议：先跑方案一（邀请有礼），验证K因子后再叠加其他玩法
================================================================================
""".format(product=product, date=today()))

