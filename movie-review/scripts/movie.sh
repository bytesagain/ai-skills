cmd_review() {
  local movie="${1:?请提供电影名}"
  local rating="${2:?请提供评分 (1-10)}"
  local keywords="${3:-}"

  # Generate stars from rating
  local stars=""
  local rating_int
  rating_int=$(awk "BEGIN{printf \"%d\", $rating}")
  for ((i=1; i<=5; i++)); do
    if (( i * 2 <= rating_int )); then
      stars+="★"
    elif (( i * 2 - 1 <= rating_int )); then
      stars+="★"
    else
      stars+="☆"
    fi
  done

  cat <<EOF
## 🎬 影评: ${movie}

**评分**: ${rating}/10 ${stars}
**关键词**: ${keywords:-未指定}
**评论日期**: $(date '+%Y-%m-%d')

---

### 📊 多维评分

\`\`\`
┌──────────────────────────────────────┐
│  ${movie} — 评分详情
├──────────────────────────────────────┤
│                                      │
│  剧本故事  ████████░░  8/10          │
│  演员表演  ███████░░░  7/10          │
│  视觉效果  █████████░  9/10          │
│  配乐音效  ████████░░  8/10          │
│  情感共鸣  ███████░░░  7/10          │
│                                      │
│  综合评分  ████████░░  ${rating}/10
│                                      │
└──────────────────────────────────────┘
\`\`\`

### ✍️ 影评框架

**1. 开头 (Hook)**
> 一句话概括观影感受或抛出问题

**2. 基本信息**
| 项目 | 内容 |
|------|------|
| 片名 | ${movie} |
| 类型 | ${keywords:-待填写} |
| 评分 | ${rating}/10 |

**3. 剧情概述 (无剧透)**
> 用2-3句话介绍电影前提，不透露关键情节

**4. 亮点分析**
- 🎭 演技: (分析主要演员表演)
- 📷 摄影: (视觉风格和画面构图)
- 🎵 配乐: (音乐对氛围的贡献)
- 📝 剧本: (故事结构和对话质量)

**5. 不足之处**
> 客观评价短板，给出具体例子

**6. 推荐总结**
EOF

  if (( rating_int >= 9 )); then
    echo "> 🏆 **强烈推荐** — 不容错过的佳作，必看！"
  elif (( rating_int >= 7 )); then
    echo "> 👍 **推荐观看** — 值得花时间的好电影"
  elif (( rating_int >= 5 )); then
    echo "> 🤔 **选择性观看** — 适合特定口味的观众"
  else
    echo "> 😐 **不太推荐** — 除非你是该类型的铁粉"
  fi
}

