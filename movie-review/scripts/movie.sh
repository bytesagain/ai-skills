cmd_analysis() {
  local movie="${1:?请提供电影名}"
  local angle="${2:?请指定分析角度 (social-class|psychology|visual|narrative|symbolism|philosophy)}"

  echo "## 🔬 深度分析: ${movie}"
  echo ""
  echo "**分析角度**: ${angle}"
  echo ""

  case "$angle" in
    social-class)
      cat <<EOF
### 社会阶层分析框架

**1. 阶层刻画**
- 上层: 空间特征、语言习惯、消费符号
- 中层: 焦虑与挣扎的表现
- 底层: 生存状态、尊严问题

**2. 阶层互动**
- 权力关系如何展现？
- 阶层流动的可能性和障碍
- 冲突的根源是什么？

**3. 符号系统**
- 空间（高处/低处 → 阶层隐喻）
- 物品（品牌、食物 → 阶层标识）
- 气味（嗅觉 → 阶层感知）

**4. 导演态度**
- 同情还是批判？
- 是否提供了解决方案？
- 留给观众什么思考？
EOF
      ;;
    psychology)
      cat <<EOF
### 心理学分析框架

**1. 角色心理画像**
- 主角的核心动机和恐惧
- 性格缺陷和盲点
- 防御机制（否认、投射、合理化）

**2. 关系动力学**
- 依恋类型（安全/焦虑/回避）
- 权力博弈和控制
- 镜像关系和投射

**3. 潜意识表达**
- 梦境/幻想场景的象征
- 重复行为模式
- 弗洛伊德式口误

**4. 创伤与治愈**
- 创伤事件及其影响
- 应对策略的有效性
- 角色弧光中的心理成长
EOF
      ;;
    visual)
      cat <<EOF
### 视觉风格分析框架

**1. 摄影构图**
- 对称/不对称构图的运用
- 景深和焦点的叙事功能
- 运镜方式与节奏

**2. 色彩运用**
- 色调与情绪对应
- 色彩变化与剧情发展
- 特定颜色的象征意义

**3. 光影设计**
- 自然光 vs 人工光
- 阴影的叙事功能
- 明暗对比的情感效果

**4. 空间设计**
- 场景选择的深层含义
- 封闭/开放空间的心理暗示
- 垂直空间（高低）的权力象征
EOF
      ;;
    *)
      cat <<EOF
### ${angle} 分析框架

**1. 主题识别**
- 核心主题是什么？
- 如何通过叙事展开？

**2. 手法分析**
- 使用了哪些表现手法？
- 这些手法的效果如何？

**3. 深层解读**
- 表面之下的含义
- 与现实的对照

**4. 评价**
- 手法是否有效？
- 在同类作品中的位置
EOF
      ;;
  esac

  echo ""
  echo "---"
  echo "💡 请基于以上框架，结合具体的电影场景和细节展开分析。"
}

