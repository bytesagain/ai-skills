### 对比维度

| 维度 | ${movieA} | ${movieB} | 胜出 |
|------|-----------|-----------|------|
| 剧本故事 | ★★★★☆ | ★★★★☆ | 平手 |
| 演员表演 | ★★★★☆ | ★★★★★ | ${movieB} |
| 视觉效果 | ★★★★★ | ★★★★☆ | ${movieA} |
| 配乐音效 | ★★★★☆ | ★★★★☆ | 平手 |
| 情感深度 | ★★★☆☆ | ★★★★★ | ${movieB} |
| 重看价值 | ★★★★★ | ★★★★☆ | ${movieA} |

### 🔍 深度对比

**相似之处**:
- (分析两部电影的共同主题)
- (共同的叙事手法)
- (相似的目标受众)

**关键差异**:
- (叙事风格差异)
- (视觉美学差异)
- (主题深度差异)
- (节奏控制差异)

### 🎯 推荐选择

| 场景 | 推荐 | 原因 |
|------|------|------|
| 想看视觉大片 | ${movieA} | 视觉冲击力更强 |
