cmd_spoiler() {
  local movie="${1:?请提供电影名}"
  local topic="${2:-结局解析}"

  cat <<EOF
## ⚠️ 剧透讨论: ${movie}

> 🚨 **以下内容包含严重剧透，未观影者请勿继续阅读！**

---

### 讨论主题: ${topic}

### 📌 讨论框架

**1. 关键剧情节点**
- 第一幕: 建置阶段的关键伏笔
- 第二幕: 转折点和冲突升级
- 第三幕: 高潮与结局

**2. ${topic}**
- 结局发生了什么？
- 为什么会这样结局？
- 是否有多重解读的可能？

**3. 隐藏细节**
- 第一遍可能忽略的线索
- 导演的暗示和伏笔
- 重看时的新发现

**4. 争议点**
- 不同观众的不同解读
- 导演的意图 vs 观众理解
- 开放式结局的可能答案

**5. 你的解读**
> 说出你的理解，和别人讨论

---

### 💬 讨论礼仪

- 🏷️ 注明剧透范围
- 🤝 尊重不同解读
- 📎 引用具体场景支持观点
- ❌ 避免人身攻击

💡 好的剧透讨论能让电影体验加倍！
EOF
}

case "$CMD" in
  review)     cmd_review "$@" ;;
  recommend)  cmd_recommend "$@" ;;
  compare)    cmd_compare "$@" ;;
  analysis)   cmd_analysis "$@" ;;
  watchlist)  cmd_watchlist "$@" ;;
  spoiler)    cmd_spoiler "$@" ;;
  help|--help) show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo "运行 'movie.sh help' 查看帮助"
    exit 1
    ;;
esac
