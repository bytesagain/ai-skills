cmd_syntax() {
cat << 'EOF'
BUILD.NINJA SYNTAX
====================

RULES & BUILD STATEMENTS:
  # Define a rule
  rule cc
    command = gcc -c $in -o $out -MD -MF $out.d
    depfile = $out.d
    description = CC $out

  rule link
    command = gcc $in -o $out
    description = LINK $out

  # Build statements
  build obj/main.o: cc src/main.c
  build obj/utils.o: cc src/utils.c
  build myapp: link obj/main.o obj/utils.o

VARIABLES:
  # Global variables
  cflags = -Wall -O2
  ldflags = -lm -lpthread

  rule cc
    command = gcc $cflags -c $in -o $out
    description = CC $out

  # Per-build variables
  build obj/debug.o: cc src/debug.c
    cflags = -Wall -g -O0           # Override for this build

SPECIAL VARIABLES:
  $in       Input files
  $out      Output files
  $in_newline  Inputs separated by newlines

SPECIAL RULES:
  # Phony targets (always out of date)
  build all: phony myapp tests

  # Default target
  default myapp

POOLS (limit parallelism):
  pool heavy_jobs
    depth = 2

  rule expensive
    command = heavy_compile $in $out
    pool = heavy_jobs

SUBNINJA:
  subninja lib/build.ninja           # Include another file

NINJA COMMANDS:
  ninja                              # Build default target
  ninja -j 16                        # 16 parallel jobs
  ninja -k 0                         # Keep going on error
  ninja -n                           # Dry run
  ninja -v                           # Verbose (show commands)
  ninja -t targets all               # List all targets
  ninja -t deps myapp                # Show deps of target
  ninja -t graph myapp | dot -Tpng > graph.png
  ninja -t clean                     # Clean all outputs
  ninja -t compdb cc                 # Compile database
  ninja -d stats                     # Build statistics
  ninja -w dupbuild=err              # Error on duplicate
EOF
}

