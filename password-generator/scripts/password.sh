#!/usr/bin/env bash
# password.sh — 密码安全工具
# 用法: bash scripts/password.sh <command>

set -euo pipefail

CMD="${1:-help}"

case "$CMD" in

generate)
cat << 'PYEOF'
## 密码生成任务

为用户生成安全的强密码。

### 操作步骤
1. 确认密码要求:
   - 长度（默认16位）
   - 是否包含大写字母（默认是）
   - 是否包含数字（默认是）
   - 是否包含特殊符号（默认是）
   - 排除歧义字符？（0O, 1lI）
   - 是否有网站特殊限制
2. 使用密码学安全的随机方法生成
3. 提供3个候选密码
4. 显示密码强度评估

### 输出格式
```
🔐 生成的密码
━━━━━━━━━━━━

  1. Kx#9mP$vR2nQ8wL4
  2. bT7@hN3&fY5jM9zA
  3. W6$pC1#kS8eR4vX2

📊 强度评估:
  长度: 16 字符
  字符集: 大写+小写+数字+符号 (94种)
  信息熵: ~105 bits
  暴力破解: 预计 10^15 年

💡 建议:
  - 使用密码管理器存储
  - 不要在多个站点重复使用
  - 启用双因素认证
```

### 注意事项
- 使用 secrets 模块而非 random
- 确保每个必需字符类别至少出现一次
- 避免生成可读的英文单词子串
PYEOF
;;

check)
cat << 'PYEOF'
## 密码强度检查任务

评估用户提供的密码的安全强度。

### 检查维度
1. **长度评分** — 越长越好
2. **字符多样性** — 大写/小写/数字/符号
3. **常见模式检测**:
   - 键盘序列 (qwerty, 1234)
   - 重复字符 (aaa, 111)
   - 常见密码 (password, admin)
   - 日期模式 (19900101)
   - 字典单词
4. **信息熵计算**
5. **已知泄露检查建议**

### 输出格式
```
🔍 密码强度检查
━━━━━━━━━━━━━━

密码: ●●●●●●●● (已隐藏, 8字符)

📊 综合评分: ██████████░░░░░ 65/100 — 中等

详细分析:
  ✅ 长度: 8字符 (及格，建议12+)
  ✅ 包含大写字母
  ✅ 包含小写字母
  ✅ 包含数字
  ❌ 缺少特殊符号
  ⚠️ 检测到键盘序列: "qwe"
  ⚠️ 包含常见单词: "pass"

🔢 信息熵: ~47 bits
⏱️ 暴力破解估计: ~2个月 (GPU集群)

🛡️ 改进建议:
  1. 增加到12+字符
  2. 添加特殊符号 (!@#$%^&*)
  3. 避免使用键盘序列
  4. 考虑使用密码短语
```
PYEOF
;;

policy)
cat << 'PYEOF'
## 密码策略建议任务

根据组织类型和安全需求，提供密码策略建议。

### 操作步骤
1. 了解组织类型（企业/学校/个人/政府）
2. 了解安全等级要求
3. 基于NIST SP 800-63B等标准推荐策略
4. 提供可直接使用的策略文档

### 输出格式
```
🏢 密码策略建议 — [组织类型]
━━━━━━━━━━━━━━━━━━━━━━━━━━

📋 基本要求:
  - 最小长度: 12字符
  - 最大长度: 128字符（支持密码管理器）
  - 字符要求: 建议但不强制混合类型

🔄 更换策略:
  - 不强制定期更换
  - 发现泄露时立即更换
  - 密码历史: 记录最近10个

🚫 黑名单:
  - 10万常见弱密码
  - 包含用户名/邮箱
  - 公司名称及变体
  - 连续/重复字符(>3)

🔐 增强措施:
  - 必须启用MFA
  - 推荐TOTP应用
  - 锁定策略: 5次失败锁15分钟
  - 登录通知

📝 用户指导:
  - 推荐密码管理器
  - 推荐密码短语
  - 禁止密码共享
  - 安全意识培训
```

### 参考标准
- NIST SP 800-63B (美国)
- 等保2.0 (中国)
- ISO 27001
- PCI DSS (支付行业)
PYEOF
;;

passphrase)
cat << 'PYEOF'
## 密码短语生成任务

生成易记又安全的密码短语。

### 操作步骤
1. 确认需求:
   - 语言（中文/英文/混合）
   - 词数（默认4-6个词）
   - 分隔符偏好
   - 是否加数字/大写增强
2. 随机选取常用词
3. 组合并评估强度

### 输出格式
```
🔑 密码短语
━━━━━━━━━━

  英文:
  1. correct-horse-battery-staple
  2. quantum-mango-velvet-thunder
  3. crystal-nebula-piano-forest

  中文拼音:
  1. putao-feiji-dianhua-yueliang
  2. xiangqi-huoshan-bingxiang-konglong

  增强版（加数字和符号）:
  1. Correct-Horse-42-Battery!
  2. Quantum.Mango.7.Velvet#

📊 强度:
  4个词 ≈ 51 bits (够用)
  5个词 ≈ 64 bits (推荐)
  6个词 ≈ 77 bits (高安全)

💡 记忆技巧:
  想象一个画面: "一匹正确的马在用电池钉钉子"
  越荒谬越好记！

⚠️ 注意:
  - 不要用歌词、名言、书名
  - 词必须随机选取，不能自己挑
  - 分隔符增加安全性
```
PYEOF
;;

bulk)
cat << 'PYEOF'
## 批量密码生成任务

一次性生成多个密码，适合团队初始化或批量创建账号。

### 操作步骤
1. 确认数量（默认10个）
2. 确认密码规格:
   - 长度
   - 字符集
   - 是否需要唯一性保证
3. 确认输出格式
4. 批量生成

### 输出格式
```
🔐 批量密码生成 (10个)
━━━━━━━━━━━━━━━━━━━━

编号  密码                  强度
────────────────────────────────
  1   Kx#9mP$vR2nQ8wL4     极强
  2   bT7@hN3&fY5jM9zA     极强
  3   W6$pC1#kS8eR4vX2     极强
  ...

📋 统计:
  生成数量: 10
  密码长度: 16
  字符集: 大小写+数字+符号
  信息熵: ~105 bits/个
  唯一性: 已验证

💾 导出建议:
  - 安全传输: 加密文件或密码管理器
  - 不要明文存储或邮件发送
  - 首次使用后要求修改
```

### 安全建议
- 批量密码通过安全渠道分发
- 强制首次登录修改密码
- 设置密码过期时间
- 记录分发日志
PYEOF
;;

audit)
cat << 'PYEOF'
## 密码安全审计任务

评估用户或组织的密码安全状况，提供改进建议。

### 审计维度
1. **密码习惯评估** — 通过问答了解
2. **密码策略审查** — 现有策略是否合理
3. **工具使用情况** — 密码管理器、2FA
4. **泄露风险** — 建议检查haveibeenpwned
5. **改进路线图** — 分优先级的行动计划

### 问答清单
```
📋 密码安全审计问卷
━━━━━━━━━━━━━━━━━━

1. 你使用密码管理器吗？
2. 有多少个账号使用相同密码？
3. 密码平均长度？
4. 是否启用了2FA？在哪些服务？
5. 上次更换重要账号密码是什么时候？
6. 是否在浏览器保存密码？
7. 是否有密码写在纸上或备忘录？
8. 公司是否有密码策略？
```

### 输出格式
```
🛡️ 密码安全审计报告
━━━━━━━━━━━━━━━━━━

📊 安全评分: ██████░░░░ 60/100

🔴 高风险:
  - 多个重要账号使用相同密码
  - 未启用银行账号的2FA

🟡 中风险:
  - 密码长度不足12位
  - 浏览器明文保存密码

🟢 良好:
  - 使用了密码管理器
  - 邮箱已启用2FA

📝 行动计划（按优先级）:
  1. [紧急] 立即更改重复使用的密码
  2. [重要] 为所有重要账号启用2FA
  3. [建议] 将所有密码迁移到密码管理器
  4. [建议] 定期检查泄露数据库
```
PYEOF
