cmd_card() {
  cat <<'EOF'
## 🃏 知识卡片生成器

请AI将书中知识点转化为间隔复习卡片：

### 卡片格式

#### 概念卡
```
📗 来源: 《[书名]》第X章
🏷️ 标签: #[主题] #[分类]

❓ 问题（正面）:
[问题描述]

💡 答案（背面）:
[简洁答案]
[补充解释]
[应用示例]
```

#### 模型卡
```
📗 来源: 《[书名]》
🏷️ 标签: #思维模型

🧠 模型名称: [名称]

📝 定义:
[一句话定义]

🔧 应用步骤:
1. ___
2. ___
3. ___

💡 应用场景:
- 场景1: ___
- 场景2: ___

⚠️ 局限性:
- ___
```

#### 金句卡
```
📗 来源: 《[书名]》第X章
🏷️ 标签: #金句

💬 金句:
> "[原文]"

🔍 解读:
[为什么重要]

🎯 应用:
[何时可以引用]
```

### 复习计划
| 复习轮次 | 间隔 | 方式 |
|----------|------|------|
| 第1次 | 当天 | 快速回顾所有卡片 |
| 第2次 | 1天后 | 测试式回顾 |
| 第3次 | 3天后 | 只复习模糊的 |
| 第4次 | 7天后 | 全部测试 |
| 第5次 | 30天后 | 最终巩固 |

### 导出格式
- Markdown文件（直接阅读）
- Anki格式（导入Anki）
- CSV格式（通用导入）
EOF
}

case "$CMD" in
  summary)    cmd_summary ;;
  keypoints)  cmd_keypoints ;;
  quotes)     cmd_quotes ;;
  review)     cmd_review ;;
  actionable) cmd_actionable ;;
  card)       cmd_card ;;
  help|--help|-h) show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo ""
    show_help
    exit 1
    ;;
esac
