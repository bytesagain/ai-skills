cmd_review() {
  cat <<'EOF'
## ✍️ 书评撰写模板

请AI根据以下模板撰写书评：

### 模板一：豆瓣风格（300-500字）
```
一句话推荐：___

这本书讲了什么：
[2-3句概括核心内容]

最打动我的3个点：
1. ___
2. ___
3. ___

不足之处：
___

适合谁读：
___

推荐指数：⭐⭐⭐⭐☆ (X/5)
```

### 模板二：专业书评（800-1200字）
```
## [书名]：[副标题/一句话评价]

### 概述
[介绍作者背景、写作动机、本书定位]

### 核心观点
[详细分析2-3个核心论点]

### 论证质量
[评价论据是否充分、逻辑是否严密]

### 创新与不足
[与同类书籍对比，指出创新点和局限]

### 目标读者
[明确适合谁、不适合谁]

### 评分
| 维度 | 评分 |
|------|------|
| 内容深度 | ⭐⭐⭐⭐⭐ |
| 可读性 | ⭐⭐⭐⭐⭐ |
| 实用性 | ⭐⭐⭐⭐⭐ |
| 创新性 | ⭐⭐⭐⭐⭐ |
| 总体 | ⭐⭐⭐⭐⭐ |
```

### 模板三：社交媒体短评（100字）
```
📚 刚读完《[书名]》

一句话：[核心价值]
最佳金句："[引用]"
适合：[目标读者]
评分：⭐⭐⭐⭐☆

#读书笔记 #[话题标签]
```
EOF
}

