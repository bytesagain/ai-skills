EOF
}

cmd_keypoints() {
  cat <<'EOF'
## 🎯 关键要点提取

请AI从书中提取核心要点：

### 提取框架

#### 💎 核心要点 (Top 5-10)

**要点 #1: [标题]**
- 📌 原文观点: [作者怎么说的]
- 🔄 我的理解: [用自己的话]
- 💡 关联知识: [与已有知识的连接]
- ⚡ 行动启发: [这个要点能让我做什么]

**要点 #2: [标题]**
- 📌 原文观点: ...
- 🔄 我的理解: ...
- 💡 关联知识: ...
- ⚡ 行动启发: ...

(重复至所有核心要点)

#### 🧩 思维模型/框架
列出书中提出的所有模型、框架、公式：
1. **[模型名]**: [简述] → 适用场景: [何时用]
2. **[框架名]**: [简述] → 适用场景: [何时用]

#### ❓ 未解答的问题
- 读完后我还想知道: ...
- 作者没有讨论到的: ...

#### 🎯 一张图总结
用文字描述本书的知识地图/逻辑框架
EOF
}

