#!/usr/bin/env bash
# reading.sh — 读书笔记助手
# Usage: bash reading.sh <command> [args...]

set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true

show_help() {
  cat <<'HELP'
Reading Notes 📚 — 读书笔记助手

用法: bash reading.sh <command> [args...]

命令:
  summary      生成结构化读书摘要
  keypoints    提取关键要点与论据
  quotes       金句摘录与解读
  review       多风格书评撰写
  actionable   从书中提取行动清单
  card         生成知识卡片(间隔复习)

示例:
  bash reading.sh summary
  bash reading.sh quotes
  bash reading.sh card

  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
HELP
}

